


class DoWhileDemo2 {
	public static void main(String[] args) {

		int x = 3;
		while(x < 3) {
			System.out.println("hello world");
			x++;
		}

		System.out.println("-----------------------");

		int y = 3;

		do {
			System.out.println("hello world");
			y++;
		} while(y < 3);

		System.out.println("----------------------");
		
	}
}
